public class Perro {
    
    String nombre;
    int edad;
    String raza;
    String color;
    String sexo;

    public Perro(String nombre, int edad, String raza, String color, String sexo) {
        
        this.nombre = nombre;
        this.edad = edad;
        this.raza = raza;
        this.color = color;
        this.sexo = sexo;

    }

}
